# quotient

def division(a, b):
    return a / b

print(division(5, 1)) # 5.0
print(division(5, 2)) # 2.5
print(division(-6, 2)) # -3.0
print(division(-6, -4)) # 1.5

# remainder

def division(a, b):
    return a % b

print(division(5, 1)) # 0
print(division(5, 2)) # 1
print(division(-6, 2)) # 0
print(division(-6, -4)) # -2