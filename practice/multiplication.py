# product

def multiplication(a, b):
    print(a * b)

multiplication(3, 8) # 24
multiplication(0, 10) # 0
multiplication(-6, 5) # -30
multiplication(-6, -5) # 30

# power

def multiplication(a, b):
    print(a ** b)

multiplication(1, 3) # 1
multiplication(2, 3) # 8
multiplication(-2, 3) # -8
multiplication(3, 0) # 1