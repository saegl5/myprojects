# product

def multiplication(a, b):
    return a * b

print(multiplication(3, 8)) # 24
print(multiplication(0, 10)) # 0
print(multiplication(-6, 5)) # -30
print(multiplication(-6, -5)) # 30

# power

def multiplication(a, b):
    return a ** b

print(multiplication(1, 3)) # 1
print(multiplication(2, 3)) # 8
print(multiplication(-2, 3)) # -8
print(multiplication(3, 0)) # 1